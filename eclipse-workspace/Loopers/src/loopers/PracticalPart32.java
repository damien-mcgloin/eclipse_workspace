public class PracticalPart32 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		for (int num = 1; num <= 10; num++) {
			if (num < 10) {
				System.out.print(num + ",");
			} else {
				System.out.println(num);

			}

		}

	}

}
