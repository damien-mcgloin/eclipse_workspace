	public static void main(String[] args) {
		
		for (int outerNum=1; outerNum<=10; outerNum++) {
			System.out.print("*"+outerNum+",");
			System.out.print("**"+outerNum+",");
			System.out.print("***"+outerNum+",");
		}

	}

