/**
 * This is a class about operators
 * @author damienmcgloin
 *
 */
public class Operators {

	/**
	 * This is the start point for op demo
	 * @param args
	 */
	public static void main(String[] args) {
		
		/*							
		 * multi line comments
		 * 
		 */
		int num1, num2, answer;
		
		// assignment of values
		num1 = 99;
		num2 = 55;
		answer = num1 + num2;
		System.out.println("Adding numbers "+answer);
		
		answer = num1 - num2;
		System.out.println("Sub numbers "+answer);
		
		answer = num1 / num2;
		System.out.println("Div numbers "+answer);
		
		answer = num1 * num2;
		System.out.println("Multi numbers "+answer);
		
		answer = num1 % num2;
		System.out.println("Mod op numbers "+answer);

	}

}
