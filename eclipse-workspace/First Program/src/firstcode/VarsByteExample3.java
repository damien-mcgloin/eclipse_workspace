package firstcode;

/**
 * This class is an example of how you declare and use a byte
 * @author damienmcgloin
 *
 */
public class VarsByteExample3 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		// declare the byte
		byte myThirdByte;
		
		// assign a value to the var
		myThirdByte = -128;

		System.out.println("Initial value : " +myThirdByte);
		
		// play with the code!!
		myThirdByte = 114;
		
		System.out.println("Then changed to : " +myThirdByte);
		
		myThirdByte = -1;
		
		System.out.println("And finally : " + myThirdByte);
	}

}
