package firstcode;

public class Decisions {

	public static void main(String[] args) {
		
		int num1, num2;
		
		num1 = 10;
		num2 = 10;
		
		if (num1 >= num2) {
			System.out.println("num 1 is bigger than or equal to num 2");
		}	
		if (num1 < num2) {
			System.out.println("num 1 is less than num 2");
		} // end of IF statement
		
		if (num1 == num2) {
			System.out.println("num1 is the same as num2");
		}
		if (num1 != num2) {
			System.out.println("num1 is not the same as num2");
		}

		System.out.println("End");
		
	} // end of method

} // end of class
